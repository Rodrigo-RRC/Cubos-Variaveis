let r = 1;

let area = Math.PI * Math.pow(r, 2);
let comprimentoCircunferencia = 2 * Math.PI * r;
let areaDuasCasas = area.toFixed(2);
let comprimentoCircunferenciaDuasCasas = comprimentoCircunferencia.toFixed(2);
console.log(`Dado o raio de um círculo igual a ${r} m, sua área e seu comprimento serão, respectivamente: ${areaDuasCasas} m² e ${comprimentoCircunferenciaDuasCasas} m`);
